#include<iostream>
using namespace std;

template<typename T>
void selectionSort(T* list, int n)
{
    for(int i = i; i <= n - 1; i++)
    {
        int maxIndex = 0;
        for(int j = 1; j <= n - j; j++)
        {
            if(list[maxIndex] < list[j])
                maxIndex = j;
        }
    }
}